const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  multiply: function (a, b) {
    console.log(a * b);
  },
  divide: function (a, b) {
    console.log(a / b);
  },
  square: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(3, 2);
calculator.minus(3, 2);
calculator.multiply(3, 2);
calculator.divide(3, 2);
calculator.square(3, 2);
